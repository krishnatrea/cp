#include<iostream>
#include<math.h>
using namespace std;
int main()
{
    int a,b,c;
    for(int i=1;i<=5;i++){
        for (int  j = 1; j <= 5; j++)
        {
            cin>>a;
            if(a==1){
                b=i;
                c=j;
            }
        }
	}
        cout<<abs(3-b)+abs(3-c);
        return 0;
}